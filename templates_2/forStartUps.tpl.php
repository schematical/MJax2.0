                <p>
                    <h3>PHASES IN A BUSINESS’S LIFE</h3>
                    We help small to medium start ups grow from Adolescence to Maturity. To find out what phase your business is in check out this except from the summary of the book <a href="#emyth"><i>E-Myth Revisited</i></a> found on <a target="blank" href="http://www.superbcoaching.com.au/Books/Summaries/The%20E-Myth%20Revisited.htm">SuperCoaching.com</a>
                </p>
                <p>
                    <h4>Infancy: The Technician’s Phase</h4>
                    The infancy stage is considered as the technician’s phase because it is the technician’s character that is most prominent on the owner at this stage. He’s main focus right now is simply to do what he wants with his business. He and his business are fused together, highly dependent on the former. You take out the owner and the business is gone.
                    In order to move on to the next cycle, the owner should realize that the business will not last if it is continuously run on this manner. He will have to realize that he has to change his approach or else his business ends there. When such realization comes, the business moves on to the adolescent stage.
                </p>
                <p>
                    <h4>Adolescence: Getting Some Help</h4>
                    When the owner starts to call for outside reinforcement, this signals that the business has moved on to the adolescent phase. Naturally, the first help he will need would have to be technical expertise from somebody who knows well his kind of business. When that somebody is found, this is where the roles of the technician, the manager and the entrepreneur are gradually introduced with each other. Then they start developing this safety area, which can be referred to as the comfort zone.
                </p>
                <p>
                    <h4>Beyond the Comfort Zone</h4>
                    There comes a point in the life of a business where limits are pushed and chaos rules over. The business owner is now dragged out of his comfort zone and is faced with the following choices: to get small again, to go for broke or to fully survive this adolescent phase and move on to the last phase.
                </p>
                <p>
                    <h4>Maturity and the Entrepreneurial Perspective</h4>
                    Although maturity is the last phase in a business’s life cycle, it is not the end of the development process. The enthusiasm for growth must still continue in order for the business to survive.
                    The entrepreneurial perspective and the entrepreneurial model for the business also reach their maturity at this phase. The former starts to assume a broader view of the business’s future while the latter, the model, becomes more balanced and could now provide the specific work assignments for all three – the technician, the manager and the entrepreneur.
                </p>
